<p>Film and television and literature may not put you in control like games do, but instead they put you outside, forcing you to take seriously the fact that the characters are <em>not</em> you, but rather someone else. Sometimes being in control is too facile, too misleading.</p>
<p>Does piloting <em>Uncharted</em>'s Nathan Drake from ledge to ledge lead to any greater understanding of his opaque motivations than watching <em>House of Cards</em>' equally impenetrable Frank Underwood? If agency means click-guessing <em>The Walking Dead</em>'s Lee Everett around his family drugstore, then maybe passivity is underrated.</p>
<p>Even games without embodied, playable first- or third-person human characters or their synecdoches are still about "you." In <em>Tetris</em> or <em>Drop7</em> or <em>Osmos</em>, you are not anyone. Rather, "you" are the pretend god in control of a manipulable world upon which meaningful force can be exerted. These are not games you might be likely to reconstruct out of paper or mashed potatoes, but you could if you set your mind to the task. They are tiny universes in which you are the prime mover, even if not the designer. You are the player, and without you the game grinds to a halt.</p>
<p>It's tempting to see <em>Proteus</em> as just another first-person art game, one that starts with conventional keyboard-and-mouse shuffle-looking and then strips away other verbs like "jump" and "shoot." Only movement remains, along with the obstreperous spacebar command to "sit," as if giving the finger to all those games in which sitting would result in an immediate bloodbath.</p>
<p>Many will dismiss <em>Proteus</em> on these grounds, concluding that it is "not a game," before launching into some tired tirade about the proper properties of genuine games: goals, choices, victory, what have you. Those players have been successfully provoked. <em>Proteus</em> intends the provocation, but doesn't do enough to follow through on it. At question is not whether the game offers sufficient choice or challenge to deserve the name "game," but <em>whose</em> choice or challenge is presented in the first place.</p>
<p>It's not the gameplay that's missing from <em>Proteus</em>. Rather, it's the <em>you</em>, the agent who would partake of it. Or, at least, in <em>Proteus</em> you are not the <em>you</em> you are used to.</p>
<p>The game loads. At first you think you are on a boat, or some sort of vessel, anyway. You look around. A misty island appears in the distance, <em>appears</em> because you can see it. You can hear the lapping water. The horizon seems to bob along to match your movements and your shifting perception. You move and look, exploring the sea, the beach, the hill, the mountain.</p>
<p>But there was no boat. It should have been your first clue, like the obvious sign at the start of an M. Night Shyamalan film -- the blatant hint that gives away the twist before you knew there was one. What can rest unperturbed on water and on earth, but still move nimbly? A spectre. A miniature hovercraft. Jesus of Nazareth.</p>
<p>Things get weirder on land. Traversing <em>Proteus</em> feels familiar, banal even. Not the space, the island itself, but the traversal. Moving, looking -- you've done it all before, inside Castle Wolfenstein, on Bob-omb Battlefield, in Rapture. But something's off this time, something subtle. Different terrains can be traversed without distinction. Hills and summits can be ascended smoothly and without struggle no matter their incline. From a distance, you see a snow-capped mountain and devise a tactic for reaching its summit. But your plan is quickly proven superfluous, as contact with the peak's foothill results in an immediate, quick assent, as if by invisible funicular.</p>
<p style="text-align: center;"><img style="margin: 5px; border: 1px solid black;" src="/db_area/images/feature/186735/proteus_shot1.jpg" alt="" width="580" height="363" /></p>
<p>What to make of it? Dismay, at first, even anger. Perhaps the creators of <em>Proteus</em> were too lazy or too inept to craft a more sophisticated locomotion system, opting instead just to couple a default camera view to first-person controls, an abstract cursor in an environment.</p>
<p>But this obvious analysis is also the wrong one. Rather than conclude that the work is incomplete or ill-conceived, why not instead assume that it means to be exactly what it is, and that it issues a challenge to those who might interact with it: to form credible theories about why it is the way it is, rather than criticisms about why it is not something else.</p>
<p>There is no "you" in <em>Proteus</em>, at least not in the way you thought there was. There is only an island. The experience you have on that island isn't an experience on an island, at all. Instead, it's an experience of an island. An island's experience. <em>Proteus</em> is a game about being an island instead of a game about being on one.</p>
<p>What does an island do? Not much, on a human scale. Islands are accreted from submarine vulcanism over hundreds of thousands of years, as tectonic plate directions shift to yield protrusions in solid, dense rock. The Big Island of Hawaii is young at some half a million years old. The oldest seamount in the Hawaiian-Emperor chain, Mejii Seamount, is at least 80 million years old.</p>
<p><em>Proteus</em> spares us the obvious portrayals of geological time, of hot spots on the Earth's mantle, of lava flows and shield building and erosion, of scientistic educationalism. Such features are not really of the island, after all, but of its creation. Just as Nate Drake isn't the same as his ontogeny from zygote to fetus to infant, so being <em>Proteus</em> isn't the same as its simulated, abstracted geological formation.</p>
<p>As for "exploration," such is the game's clever conceit, the ruse that tricks you into thinking the work is about <em>you</em>, into thinking that you are there at all. <em>Proteus</em> meets you partway, offering the appearance of changes in movement, of changes in view, of the ability to "sit." But these are just metaphors, the minimum necessary invitation to provide you, the human player, a satisfactory analogy through which to grasp the island's existence as island.</p>
<p>The arbitrary configurations of a computer interface, whose careless tousles along a 3D vector happen to correspond with the usual manner in which a player might navigate a virtual world. One explores <em>Proteus</em> less like one explores a wooded nature preserve and more like one explores a naked body -- by moving it through one's attention rather than by moving one's attention through it.</p>
<p>In <em>Proteus</em>, we find something in between the personal time of human agency and the historical time of tectonic effects. Day and night doesn't pass, so much as the island dresses in day and night's clothing. Night doesn't descend upon the island so much as <em>the island nights</em>, like the squirrel scurries or the leaves fall. If tousled in the right way, it relents, donning the garb of different seasons. Time doesn't pass upon it any more than you move around it. It is you who is too dense, too stuck in your own ape body that you require time to pass before your senses kick in.</p>
<p>Islands. They are a common staple of video games: <em>Myst</em>, <em>Uncharted</em>, <em>The Secret of Monkey Island</em>. Yet, we don't think much about these islands themselves. Even in a game like <em>Far Cry</em>, in which the environment has a much larger role to play, that environment is still rendered <em>for you</em>, you the playable character and for you the human player. <em>Proteus</em>'s island isn't for <em>you</em> at all. It isn't concerned with your attention span or your expectations. It's just there. Just there, until it gets bored and turns you off.</p>

