<h1><strong>One: Nil Person</strong></h1>
<p>Video games are narcissistic. They are about you, even when they put you in someone else's shoes. You are a space marine among hell spawn. You are a mafioso just released from prison. You are a bear with a bird in your backpack. You are a Tebowing Tim Tebow. We may think we play video games to be someone or something else, but inevitably we do so to be ourselves as well -- ourselves in the guise of someone else.</p>
